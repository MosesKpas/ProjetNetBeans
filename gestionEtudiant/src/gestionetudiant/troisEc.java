    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        File p = new File("C:/Users/Moses/Desktop/BASE/COURS/"+titre.getText());
        File save = new File("C:/Users/Moses/Desktop/BASE/COURS/"+titre.getText()+"/"+numc.getText()+".txt");
        if (!save.exists() && p.mkdir()) {
            try {
                p.getAbsoluteFile();
                save.getAbsolutePath();
                save.createNewFile();
                FileWriter writer = new FileWriter(save);
                BufferedWriter bw = new BufferedWriter(writer);
                bw.write("Num cours: " + numc.getText());
                bw.newLine();
                bw.write("Code cours: " + code.getText());
                bw.newLine();
                bw.write("Titre: " + titre.getText());
                bw.newLine();
                bw.close();
                writer.close();

            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

