    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        File p = new File("C:/Users/Moses/Desktop/BASE/ETUDIANTS/"+nom.getText());
        File save = new File("C:/Users/Moses/Desktop/BASE/ETUDIANTS/"+nom.getText()+"/"+num.getText()+".txt");
        if (!save.exists() && p.mkdir()) {
            try {
                p.getAbsoluteFile();
                save.getAbsolutePath();
                save.createNewFile();
                FileWriter writer = new FileWriter(save);
                BufferedWriter bw = new BufferedWriter(writer);
                bw.write("Num: " + num.getText());
                bw.newLine();
                bw.write("Nom: " + nom.getText());
                bw.newLine();
                bw.write("Prenom: " + pre.getText());
                bw.newLine();
                bw.close();
                writer.close();

            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        JOptionPane.showMessageDialog(null,"Compte creer");

    }//GEN-LAST:event_jButton1ActionPerformed


