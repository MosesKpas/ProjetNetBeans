    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //ENREGISTREMENT DES ETUDIANTS

        //Try{logique}catch(Exception){msg}: c'est la gestion des erreurs
        try {
            //Recuperations des inputs(Textfield) dans des variables
            int Mat = Integer.parseInt(matricule_save.getText());
            String Nom = nom_save.getText();
            String Post = post_save.getText();
            String Pre = pre_save.getText();
            String Sexe = sexe_save.getSelectedItem().toString();
            String Fac = faculte_save.getSelectedItem().toString();
            String Prom = promo_save.getSelectedItem().toString();
            String Com = com_save.getSelectedItem().toString();
            String Quart = quart_save.getText();
            String Av = avenue_save.getText();
            String Etat = "NON_SOLDER";
            int Num = Integer.parseInt(num_save.getText());
            int Fr = Integer.parseInt(Frais.getText());
            int Payer = 0;
            String Bis = "";

            //Si on valide le BIS
            if (bis.isSelected()) {
                Bis = bis.getText();
            } else {
                Bis = "";
            }
            //Si il y a un caractere special
            if (Nom.contains("'") || Nom.contains("\"") || Post.contains("'") || Post.contains("\"")
                    || Pre.contains("'") || Pre.contains("\"") || Quart.contains("'")
                    || Quart.contains("\"") || Nom.contains("'") || Av.contains("'")
                    || Nom.contains("'") || Av.contains("\"")) {
                JOptionPane.showMessageDialog(null, "Attention au caractere special * ' * ");
            } //si il ne valide pas le sexe
            else if (Sexe.contains("SEXE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir le sexe SVP!");
            } //Si il ne valide pas la faculte
            else if (Fac.contains("FACULTE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir la faculte SVP!");
            } //si il ne valide pas la commune
            else if (Com.contains("COMMUNE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir la commune SVP!");
            } else if (filename == null) {
                JOptionPane.showMessageDialog(null, "ERREUR\nInserer une photo");
            } //Sinon on enregistre les informations dans la base de donnee
            else {
                InputStream img = new FileInputStream(new File(filename));
                Enregistrer(Mat, Nom, Post, Pre, Sexe, Prom, Fac, Com, Quart, Av, Num, Bis, Fr, Payer, img, Etat);
                JOptionPane.showMessageDialog(null, "L'etudiant " + Nom + " doit se rendre a la caisse\n"
                        + "Doit solder " + Frais.getText() + " $");
                //Apres enregistrement on vide les inputs
                matricule_save.setText("");
                nom_save.setText("");
                post_save.setText("");
                pre_save.setText("");
                sexe_save.setSelectedIndex(0);
                faculte_save.setSelectedIndex(0);
                promo_save.removeAllItems();
                promo_save.addItem("PROMOTION");
                com_save.setSelectedIndex(0);
                quart_save.setText("");
                avenue_save.setText("");
                num_save.setText("");
                voir_img.setIcon(null);
                if (bis.isSelected() == true) {
                    bis.setSelected(false);
                }
                //supprimer les infos du tableau
                delInfoTab();
                data();
                delInfoTab_MODIF();
                dataPourModifier();
                dataCaisse();
                compterSexesF();
                compterSexesM();

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "ERREUR\nVerifier les zones de saisie svp ");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

