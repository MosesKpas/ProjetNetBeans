    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        //Modification
        try {
            int Mat = Integer.parseInt(matricule_modif.getText());
            String Nom = nom_modif.getText();
            String Post = post_modif.getText();
            String Pre = pre_modif.getText();
            String Sexe = sexe_modif.getSelectedItem().toString();
            String Fac = faculte_modif.getSelectedItem().toString();
            String Prom = promo_modif.getSelectedItem().toString();
            String Com = com_modif.getSelectedItem().toString();
            String Quart = quart_modif.getText();
            String Av = avenue_modif.getText();
            int Num = Integer.parseInt(num_modif.getText());
            int Fr = Integer.parseInt(Frais_modif.getText());
            int Payer = 0;
            String Bis = "";

            //Si on valide le BIS
            if (bis.isSelected()) {
                Bis = bis.getText();
            } else {
                Bis = "";
            }
            //Si il y a un caractere special
            if (Nom.contains("'") || Nom.contains("\"") || Post.contains("'") || Post.contains("\"")
                    || Pre.contains("'") || Pre.contains("\"") || Quart.contains("'")
                    || Quart.contains("\"") || Nom.contains("'") || Av.contains("'")
                    || Nom.contains("'") || Av.contains("\"")) {
                JOptionPane.showMessageDialog(null, "Attention au caractere special ' ");
            } //si il ne valide pas le sexe
            else if (Sexe.contains("SEXE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir le sexe SVP!");
            } //Si il ne valide pas la faculte
            else if (Fac.contains("FACULTE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir la faculte SVP!");
            } else if (Prom.contains("PROMOTION")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir la Promotion SVP!");
            } //si il ne valide pas la commune
            else if (Com.contains("COMMUNE")) {
                JOptionPane.showMessageDialog(null, "ERREUR\nChoisir la commune SVP!");
            } else {
                if (!Fac.equals(facmemo.getText())) {
                    int option = JOptionPane.showConfirmDialog(null, "ATTENTION\n\nChangement de Faculte pour :"
                            + "\n " + Nom, "MOFIFICATION", JOptionPane.INFORMATION_MESSAGE);
                    if (option == JOptionPane.OK_OPTION) {
                        String Etat = "NON_SOLDER";
                        Modifier_etudiants_solde(Mat, Nom, Post, Pre, Sexe, Prom, Fac, Com, Quart, Av, Num, Bis, Fr, Payer, Etat);
                    } else {
                        JOptionPane.showMessageDialog(null, "Choisir la faculte " + facmemo.getText() + " avant de poursuivre");
                    }
                } else if (!Frais_modif.getText().equals(frais_mod.getText())) {
                    int option = JOptionPane.showConfirmDialog(null, "ATTENTION\n\nChangement du FRAIS-ACADEMIQUE pour :"
                            + "\n " + Nom, "MOFIFICATION", JOptionPane.INFORMATION_MESSAGE);
                    if (option == JOptionPane.OK_OPTION) {
                        String Etat = "Non_SOLDER";
                        Modifier_etudiants_solde(Mat, Nom, Post, Pre, Sexe, Prom, Fac, Com, Quart, Av, Num, Bis, Fr, Payer, Etat);
                    } else {
                        JOptionPane.showMessageDialog(null, "Saisir " + frais_mod.getText() + " avant de poursuivre");
                    }
                } else if (!Prom.equals(promo_mod.getText())) {
                    int option = JOptionPane.showConfirmDialog(null, "ATTENTION\n\nChangement de Promotion pour :"
                            + "\n " + Nom, "MOFIFICATION", JOptionPane.INFORMATION_MESSAGE);
                    if (option == JOptionPane.OK_OPTION) {
                        String Etat = "NON_SOLDER";
                        Modifier_etudiants_solde(Mat, Nom, Post, Pre, Sexe, Prom, Fac, Com, Quart, Av, Num, Bis, Fr, Payer, Etat);
                    } else {
                        JOptionPane.showMessageDialog(null, "Choisir la Promo " + promo_mod.getText() + " avant de poursuivre");
                    }
                } else {
                    Modifier_etudiants(Mat, Nom, Post, Pre, Sexe, Prom, Fac, Com, Quart, Av, Num, Bis);
                }
                matricule_modif.setText("");
                nom_modif.setText("");
                post_modif.setText("");
                pre_modif.setText("");
                sexe_modif.setSelectedIndex(0);
                faculte_modif.setSelectedIndex(0);
                promo_modif.setSelectedIndex(0);
                com_modif.setSelectedIndex(0);
                quart_modif.setText("");
                avenue_modif.setText("");
                num_modif.setText("");
                if (bis.isSelected() == true) {
                    bis.setSelected(false);
                }
                delInfoTab_MODIF();
                dataPourModifier();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur\n" + e);
        }
    }//GEN-LAST:event_jButton15ActionPerformed

