    public void dataPatient() {
        try {
            ouverture();
            this.st = (Statement) this.con.createStatement();
            String sql = "select * from alls";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                String NOM = rs.getString("identite");
                String NUMERO = rs.getString("telephone");
                String COM = rs.getString("commune");
                String QU = rs.getString("quartier");
                String AV = rs.getString("avenue");
                String NU = rs.getString("numero");
                String tdData[] = {NOM, NUMERO, COM + " " + QU + " " + AV + " " + NU};
                DefaultTableModel tbModel = (DefaultTableModel) tab.getModel();
                tbModel.addRow(tdData);
            }
            fermeture();
        } catch (Exception e) {
        }
    }
