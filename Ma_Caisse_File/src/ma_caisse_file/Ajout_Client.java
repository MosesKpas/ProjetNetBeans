    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //pour la sauvegarde:
        save = new File("C:/Users/Moses/Desktop/School/java/polo.txt");
        if (!save.exists()) {
            try {
                save.createNewFile();
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else {
            try {
                FileWriter writer = new FileWriter(save);
                BufferedWriter bw = new BufferedWriter(writer);
                bw.write("Nom: " + nom.getText());
                bw.newLine();
                bw.write("Postnom: " + post.getText());
                bw.newLine();
                bw.write("Prenom: " + pre.getText());
                bw.newLine();
                bw.write("Numero: " + num.getText());
                bw.newLine();
                bw.write("Adresse: " + adresse.getText());
                bw.newLine();
                bw.write("Motde passe: " + motdepasse.getText());
                bw.close();
                writer.close();
                

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

