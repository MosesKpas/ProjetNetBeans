    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        try {
            //Recuperation des informations
            String no,post,pre,mdp,confmdp,fonction,mat;
            no=boxNomUtilisateur.getText();
            post=boxPostnomUtilisateur.getText();
            pre=boxPrenomUtilisateur.getText();
            mdp=boxMdpUtilisateur.getText();
            confmdp=boxConfirmMdpUitilisateur.getText();
            fonction= (String) boxFonctionUtilisateur.getSelectedItem();
            
            // verification des cases vides
            if(no.isEmpty()||post.isEmpty()|| pre.isEmpty()||mdp.isEmpty()||confmdp.isEmpty()){
                JOptionPane.showMessageDialog(null,"Veuillez remplir tout les champs");
            }
            else if(!mdp.equals(confmdp)){
                JOptionPane.showMessageDialog(null,"Votre mot de passe ne correspond pas a celui que vous confirmez");
            }
            else if(mdp.equals(confmdp) && mdp.length()<5){
                JOptionPane.showMessageDialog(null,"Veuillez inserer un mot de passe superieur a 5 characteres");
            }
            else{
               // creation du matricule
                
                String firstTwoLetters = no.substring(0, 2);
                String ffff=pre.substring(1, 3);
                boxMatriculeUtilisateur.setText(firstTwoLetters+ffff+"024");
                mat=boxMatriculeUtilisateur.getText();
                
                enregistrerUtilisateur(no, post, pre, mdp, mat, fonction);
            }
            
        } catch (Exception e) {JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton16ActionPerformed

