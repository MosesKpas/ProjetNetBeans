    public void table(){
        String[]ventes = {"NOM","ADRESSE","PRODUIT","COÛT"};
        String[]afficher = new String[5]; 
        DefaultTableModel model = new DefaultTableModel(null, ventes);
        try {
            connect();
            Statement st = (Statement) con.createStatement();
            rs = st.executeQuery("select * from ventes");
            while(rs.next()){
                afficher[0] = rs.getString("nom");
                afficher[1] = rs.getString("adresse");
                afficher[2] = rs.getString("produit");
                afficher[3] = rs.getString("payer");
                model.addRow(afficher);
            }
            jTable1.setModel(model);
                con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur trouvée ici "+e.getMessage());
            e.printStackTrace();
        }
    }
