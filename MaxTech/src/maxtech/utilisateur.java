public void verifier(String Nom, String Code) throws SQLException{
    try {
        connecter();
    Statement stm=(Statement) con.createStatement();
    String rqt="Select*from user where nom = '"+Nom+"'and motdepasse = '"+Code+"'";
    ResultSet rs=stm.executeQuery(rqt);
    if(rs.next()){
        this.dispose();
        new menu().setVisible(true);
    }else{
        JOptionPane.showMessageDialog(this, "Nom ou Mot de passe incorrect");
        VNom.setText("");
        VPass.setText(null);
    }
    con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erreur trouvée ici "+e.getMessage());
            e.printStackTrace();
    }
    
    
}
