    private void blActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blActionPerformed
        // TODO add your handling code here:
        if (sh.getBackground() == Color.black) {
            tr.setText("True");
            int t;
            t = Integer.parseInt("" + truec.getText());
            t = t + 1;
            truec.setText(""+t);
            fl.setText(null);
            sh.setBackground(couleurAleatoire());
        }
        else if (sh.getBackground() != Color.black) {
            fl.setText("False");
            int f;
            f = Integer.parseInt("" + falsec.getText());
            f = f + 1;
            falsec.setText("" + f);
            tr.setText(null);

            sh.setBackground(couleurAleatoire());
        }
    }//GEN-LAST:event_blActionPerformed

