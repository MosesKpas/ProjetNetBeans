    private Color couleurAleatoire() {
        Random rand = new Random();
        switch (rand.nextInt(6)) {
            case 0:
                return Color.RED;
            case 1:
                return Color.black;
            case 2:
                return Color.green;
            case 3:
                return Color.blue;
            case 4:
                return Color.orange;
            default:
                return Color.gray;

        }
    }

