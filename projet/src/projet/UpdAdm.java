    private void VConnexActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VConnexActionPerformed
        try {
            String nom = VNom.getText();

            String pass = VPass.getText();
            String cpass = VCPass.getText();
            if (nom.isEmpty() || pass.isEmpty() || pass.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Erreur de Saisi", null, JOptionPane.ERROR_MESSAGE);
                VNom.setText(null);
                VPass.setText(null);
                VCPass.setText(null);

            } else if(VNom.getText().contains("'")) {
                JOptionPane.showMessageDialog(null, "Pas des caracteres speciaux");
                VNom.setText(null);
                VPass.setText(null);
                VCPass.setText(null);
            } else {
                if (cpass.equals(pass)) {

                    try {
                        connecter();
                        pst = (PreparedStatement) con.prepareStatement("update useradm set nom=?, pass=? where 1");
                        pst.setString(1,VNom.getText());
                        pst.setString(2,VCPass.getText());

                        pst.executeUpdate();
                        con.close();

                        menuadm m = new menuadm();
                        m.setVisible(true);
                        m.setLocationRelativeTo(null);
                        m.setResizable(true);
                        this.setExtendedState(MAXIMIZED_BOTH);
                        this.dispose();
                    } catch (Exception e) {
                        System.out.println("erreur "+e);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Erreur de code", null, JOptionPane.ERROR_MESSAGE);

                    VPass.setText(null);
                    VCPass.setText(null);
                }
            }

        } catch (Exception e) {
            System.out.println("erreur "+e);

        }
    }//GEN-LAST:event_VConnexActionPerformed

