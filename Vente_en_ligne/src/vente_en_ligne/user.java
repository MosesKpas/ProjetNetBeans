    public void save(String Nom, String Pre,String Code,String Genre,String Num,String Com,String Quart,int Nump){
 try {
        ouverture();
        this.st=(Statement)this.con.createStatement();
        this.requetteAide="insert into user value('"+Nom+"','"+Pre+"','"+Code+"','"+Genre+"','"+Num+"','"+Com+"','"+Quart+"','"+Nump+"')";
        this.st.executeUpdate(requetteAide);
        JOptionPane.showMessageDialog(null,"Enreistrer");
        fermeture();
    } catch (Exception e) {JOptionPane.showMessageDialog(null,"Pas d'enregistrement"+e);
    }}

public void verf(String Nom, String Code){
    try {
        ouverture();
        this.st=(Statement)this.con.createStatement();
        this.requetteVerifier="select * from user where nom='"+Nom+"' and Code ='"+Code+"'";
        ResultSet rs= this.st.executeQuery(requetteVerifier);
        if(rs.next()){
        dispose();
        new accueil().setVisible(true);
        JOptionPane.showMessageDialog(null,"Bienvenue "+nom.getText());
        }
        else{JOptionPane.showMessageDialog(null,"Pas d'info");}
        fermeture();
    } catch (Exception e) {JOptionPane.showMessageDialog(null,""+e.getMessage());
    }
}
